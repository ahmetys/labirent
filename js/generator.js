    generateMaze() {
        this.mazeArray[this.randomNumber(5)].forEach((col, index) => {
            let colDiv = document.createElement("div");
            colDiv.className = "col";
            colDiv.setAttribute("data-col", index);
            col.forEach((cell, cindex) => {
                let cellDiv = document.createElement("div");
                cellDiv.className = "cell";
                cellDiv.setAttribute("data-col", index);
                cellDiv.setAttribute("data-cell", cindex)
                if (cell == 0) {
                    cellDiv.style.background = "#000000";
                } else {
                    cellDiv.style.background = "#ffffff";
                }
                colDiv.appendChild(cellDiv)
            })
            ui.map.appendChild(colDiv)
        })
        const player = document.createElement("div");
        player.className = "player";
        document.querySelector("div[data-col='19'][data-cell='20']").appendChild(player)
        document.querySelector("div[data-col='1'][data-cell='0']").setAttribute("data-finish", "")

    }
